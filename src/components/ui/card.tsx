import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: 'default' | 'glow' | 'danger' | 'success' | 'warning';
  }
>(({ className, variant = 'default', ...props }, ref) => {
  const variants = {
    default: "bg-card border-border",
    glow: "bg-card border-primary/30 border-glow",
    danger: "bg-gradient-danger border-destructive/30 border-glow-danger",
    success: "bg-gradient-success border-success/30 border-glow-success",
    warning: "bg-card border-warning/30",
  };

  return (
    <div
      ref={ref}
      className={cn(
        "rounded-lg border text-card-foreground shadow-card transition-all duration-300",
        variants[variant],
        className
      )}
      {...props}
    />
  );
});
Card.displayName = "Card";

